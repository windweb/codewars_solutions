def points(games):
    result = []
    count = 0
    for i in games:
        z = list(i)
        x = z[0]
        y = z[-1]
        if x > y:
            result.append(3)
        elif x < y:
            result.append(0)
        else:
            result.append(1)
        count += 1
    print(sum(result))


points(['1:0','2:0','3:0','4:0','2:1','3:1','4:1','3:2','4:2','4:2'])  # 30
points(['1:1', '2:2', '3:3', '4:4', '2:2', '3:3', '4:4', '3:3', '4:4', '4:4'])  # 10
points(['0:1','0:2','0:3','0:4','1:2','1:3','1:4','2:3','2:4','3:4'])  # 0
points(['1:0','2:0','3:0','4:0','2:1','1:3','1:4','2:3','2:4','3:4'])  # 15
points(['1:0','2:0','3:0','4:4','2:2','3:3','1:4','2:3','2:4','3:4'])  # 12